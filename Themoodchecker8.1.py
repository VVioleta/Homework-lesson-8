mood = input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("It will get better!")
elif mood == "excited":
    print("That is amazing!")
elif mood == "relaxed":
    print("Enjoy it!")
else:
    print("I don't recognize this mood.")




